let currentLocale: Locale = 'en';

export function getLocale(): Locale {
    return currentLocale;
}

export function setLocale(locale: Locale): void {
    currentLocale = locale;
    try {
        localStorage.setItem('fyk_locale', locale);
        const rtlLocale = LOCALES.find(l => l.code === locale);
        document.documentElement.dir = rtlLocale?.rtl ? 'rtl' : 'ltr';
        document.documentElement.lang = locale;
    } catch {
    }
}

export function initLocale(): Locale {
    try {
        const stored = localStorage.getItem('fyk_locale') as Locale | null;
        if (stored && LOCALES.find(l => l.code === stored)) {
            setLocale(stored);
            return stored;
        }
        const browser = navigator.language.split('-')[0] as Locale;
        if (LOCALES.find(l => l.code === browser)) {
            setLocale(browser);
            return browser;
        }
    } catch {
    }
    return 'en';
}

export function t(key: string, fallback?: string): string {
    const translations = TRANSLATIONS[currentLocale] || TRANSLATIONS.en;
    return translations[key] ?? TRANSLATIONS.en[key] ?? fallback ?? key;
}

export function formatCurrency(amount: number, currencyCode = 'USD'): string {
    const currency = CURRENCIES[currencyCode] || CURRENCIES.USD;
    try {
        return new Intl.NumberFormat(currentLocale, {
            style: 'currency',
            currency: currency.code,
            maximumFractionDigits: 2,
        }).format(amount);
    } catch {
        return `${currency.symbol}${amount.toFixed(2)}`;
    }
}
